#ifndef ATOL_H
#define ATOL_H

/**
 * @brief Converts a string to a long integer.
 *
 * The atol function converts the initial portion of the string pointed to by `str` to a long integer representation.
 *
 * @param str The string to be converted.
 * @return The converted long integer value.
 */
long int atol(const char *str);

#endif /* ATOL_H */